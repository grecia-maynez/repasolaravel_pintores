    <p>MAGDALENA CARMEN FRIDA KAHLO CALDERÓN nació el 6 de julio de 1907 en la Ciudad de México, en la casa que fuera propiedad de sus padres desde 1904, y que hoy se conoce como la Casa Azul. Hija de Wilhelm (Guillermo) Kahlo, de ascendencia húngaro-alemana, y Matilde Calderón, originaria de Oaxaca, Frida fue la tercera de cuatro hijas. Sus dos hermanas, Matilde y Adriana, fueron las mayores y Cristina, la menor.
    <br>
    A los seis años Frida enfermó de poliomielitis, causándole que su pierna derecha quedara más corta; esto fue motivo de burlas. Sin embargo, estos sucesos no le impidieron ser una estudiante inquieta y tenaz. Realizó sus estudios en la Escuela Nacional Preparatoria.
    <br>
    A los 18 años, el 17 de septiembre de 1925, Frida tuvo un trágico accidente. El autobús en el que viajaba fue arrollado por un tranvía. Las consecuencias para ella fueron graves: fractura de varios huesos y lesiones en la espina dorsal. Debido a la inmovilidad a la que se vio sometida varios meses, Frida comenzó a pintar. Así, se relaciona con varios artistas, entre ellos la fotógrafa Tina Modotti y el entonces ya reconocido artista Diego Rivera.</p>
    <br>
    Llevó una vida poco convencional.Su obra pictórica gira temáticamente en torno a su biografía y a su propio sufrimiento. Fue autora de 150 obras, principalmente autorretratos, en los que proyectó sus dificultades por sobrevivir. La obra de Frida y la de su marido, el pintor Diego Rivera, se influyeron mutuamente. Ambos compartieron el gusto por el arte popular mexicano de raíces indígenas, inspirando a otros pintores mexicanos del periodo posrevolucionario.
